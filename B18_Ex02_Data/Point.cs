using System;
using System.Collections.Generic;
using System.Text;

namespace B18_Ex02_Data
{
	internal class Point
	{
		private int x;
		private int y;

		public int X
		{
			get
			{
				return x;
			}
			set
			{
				x = value;
			}
		}

		public int Y
		{
			get
			{
				return y;
			}
			set
			{
				y = value;
			}
		}
	}
}
